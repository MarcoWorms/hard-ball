const state =
{
  ////////////////////////////////////////////////////////////////////////////// S.create
  //
  create: () =>
  {
    localStorage.setItem( '0', JSON.stringify( change ) )

    if( '1' in localStorage )
    {
      change = JSON.parse( localStorage.getItem( '1' ) )
    }
  },

  ////////////////////////////////////////////////////////////////////////////// S.update
  //
  update: () =>
  {
    localStorage.setItem( '1', JSON.stringify( change ) )
  },
}

//////////////////////////////////////////////////////////////////////////////// ENGINE
//
const engine =
{
  ////////////////////////////////////////////////////////////////////////////// E.create
  //
  create: () =>
  {
    state.create()
  },

  ////////////////////////////////////////////////////////////////////////////// E.update
  //
  update: () =>
  {
    state.update()

    window.requestAnimationFrame( engine.update )
  },
}

//////////////////////////////////////////////////////////////////////////////// CALL
//
engine.create()
engine.update()
